public class ShapeFactory {

    // Factory Method
    public Shape getShape(String shapeType) {
        if (shapeType == null) {
            return null;
        }
        switch (shapeType.toLowerCase()) {
            case "circle":
                return new Circle();
            case "rectangle":
                return new Rectangle();
            case "square":
                return new Square();
            default:
                System.out.println("Unknown Shape Type!");
                return null;
        }
    }
}